"""
在使⽤变量时，务必牢记下述规则。
1.变量名只能包含字⺟、数字和下划线 。变量名能以字⺟或下划线打头，但不能以数字打头。
2.变量名不能包含空格，但能使⽤下划线来分隔其中的单词。
3.不要将 Python 关键字和函数名⽤作变量名。
4.慎⽤⼩写字⺟ l 和⼤写字⺟ O，因为它们可能被⼈错看成数字 1 和0。
"""
# 练习 2.1：简单消息 将⼀条消息赋给变量，并将其打印出来
message = "hello world"
print(message)

# 练习 2.2：多条简单消息 将⼀条消息赋给变量，并将其打印出来；再
# 将变量的值修改为⼀条新消息，并将其打印出来。
message1 = "hello python"
print(message1)
message1 = "hello nike"
print(message1)
